public class Product {

    private double basePrice;
    private int stockLevel;

    public double getBasePrice() {
        return basePrice;
    }

    public int getStockLevel() {
        return stockLevel;
    }

    public void setBasePrice(double p) {
        basePrice = p;
    }

    public void setStockLevel(int s) {
        stockLevel = s;
    }
}
